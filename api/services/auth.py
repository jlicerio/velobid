def mask_email(email: str) -> str:
    """Show only the first two characters + ``***`` + domain.

    Examples:
        ``jose@airhero.com`` -> ``jo***@airhero.com``
        ``a@b.com``          -> ``a***@b.com``
    """
    local, at, domain = email.partition("@")
    show = local[:2] if len(local) > 2 else local
    return f"{show}***{at}{domain}"


def get_pending_signup_by_email(email: str) -> PendingSignup | None:
    """Look up a pending (or previously-pending) signup by admin email."""
    return _pending_signups.get(email)


def get_pending_signup_by_user_id(user_id: str) -> PendingSignup | None:
    """Look up a pending signup by the admin user's UUID."""
    for signup in _pending_signups.values():
        if signup.admin_user_id == user_id:
            return signup
    return None


def _store_signup(signup: PendingSignup) -> None:
    """Store a pending signup keyed by both email and signup ID."""
    _pending_signups[signup.admin_email] = signup
    _pending_signups[signup.signup_id] = signup


def _remove_signup(signup: PendingSignup) -> None:
    """Remove a pending signup from the in-memory store."""
    _pending_signups.pop(signup.admin_email, None)
    _pending_signups.pop(signup.signup_id, None)


